// Generates scripts/sql/seed_projects.sql from src/data/projects.ts + the
// existing /public/images/projects/<slug>-large.jpg /-thumb.jpg files.
//
// Run once (or whenever src/data/projects.ts / the images change):
//   node scripts/generate-seed-sql.mjs
//
// The resulting file can be pasted into phpMyAdmin's "SQL" tab, or imported
// via the "Import" tab (recommended if it exceeds the SQL box's size limit).

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { projects } from "../src/data/projects.ts";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const imagesDir = path.join(root, "public", "images", "projects");
const outFile = path.join(root, "scripts", "sql", "seed_projects.sql");

function sqlString(value) {
  return `'${value.replace(/\\/g, "\\\\").replace(/'/g, "''")}'`;
}

function hexLiteral(buffer) {
  return `0x${buffer.toString("hex")}`;
}

function loadImage(slug, variant) {
  const file = path.join(imagesDir, `${slug}-${variant}.jpg`);
  if (!existsSync(file)) {
    console.warn(`  ! missing ${file}, storing NULL for ${variant}`);
    return null;
  }
  return readFileSync(file);
}

const lines = [];
lines.push("-- Generated by scripts/generate-seed-sql.mjs — do not edit by hand.");
lines.push("-- Import this AFTER running create_projects_table.sql.");
lines.push("");
lines.push("INSERT INTO `projects`");
lines.push(
  "  (`slug`, `title`, `tags`, `description`, `image_large`, `image_large_mime`, `image_thumb`, `image_thumb_mime`, `sort_order`)",
);
lines.push("VALUES");

const rows = projects.map((project, index) => {
  const large = loadImage(project.slug, "large");
  const thumb = loadImage(project.slug, "thumb");

  const values = [
    sqlString(project.slug),
    sqlString(project.title),
    sqlString(JSON.stringify(project.tags)),
    sqlString(project.desc),
    large ? hexLiteral(large) : "NULL",
    large ? sqlString("image/jpeg") : "NULL",
    thumb ? hexLiteral(thumb) : "NULL",
    thumb ? sqlString("image/jpeg") : "NULL",
    String(index),
  ];

  return `  (${values.join(", ")})`;
});

lines.push(
  rows.join(",\n") +
    "\nON DUPLICATE KEY UPDATE\n  `title` = VALUES(`title`),\n  `tags` = VALUES(`tags`),\n  `description` = VALUES(`description`),\n  `image_large` = VALUES(`image_large`),\n  `image_large_mime` = VALUES(`image_large_mime`),\n  `image_thumb` = VALUES(`image_thumb`),\n  `image_thumb_mime` = VALUES(`image_thumb_mime`),\n  `sort_order` = VALUES(`sort_order`);",
);

writeFileSync(outFile, lines.join("\n") + "\n");
console.log(`Wrote ${outFile} (${(lines.join("\n").length / 1024 / 1024).toFixed(2)} MB)`);
